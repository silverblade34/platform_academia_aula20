<template>
    <div class="p-4 rounded-lg bg-white shadow-sm min-h-[15rem]">
        <Bar :data="data" :options="options" />
    </div>
</template>
<script>
import {
    Chart as ChartJS,
    Title,
    Tooltip,
    Legend,
    BarElement,
    CategoryScale,
    LinearScale
} from 'chart.js'
import { Bar } from 'vue-chartjs'

